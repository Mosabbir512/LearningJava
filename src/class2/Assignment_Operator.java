package class2;

public class Assignment_Operator {
    public static void main(String[] args) {

        int a = 10;

        a += 5;
        System.out.println(a);

        a -= 3;
        System.out.println(a);

        a *= 2;
        System.out.println(a);

        a /= 4;
        System.out.println(a);
    }
}
