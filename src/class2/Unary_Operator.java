package class2;

public class Unary_Operator {
    public static void main(String[] args) {

        int a = 10;

        System.out.println("Original: " + a);
        System.out.println("Post Increment: " + (a++));
        System.out.println("After Increment: " + a);
        System.out.println("Pre Decrement: " + (--a));
    }
}
